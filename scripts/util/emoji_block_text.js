let fill = ':shipit:';
let blank = ':blank:';

const input = process.argv[2].toUpperCase();
if (process.argv[3]) {
  fill = `:${process.argv[3]}:`;
}
if (process.argv[4]) {
  blank = `:${process.argv[4]}:`;
}

for(var i=0;i<5;i++) {
  const char = input.split('');
  for (var c = 0;c<char.length; c++) {
    let m = chars[char[c]];
    if (m)
    {
      for(var x=0;x<3;x++) {
        process.stdout.write(m[i][x] ? fill : blank);
      }
      if (c < (char.length-1)) process.stdout.write(blank);
    }
  }
  process.stdout.write('\r\n');
}